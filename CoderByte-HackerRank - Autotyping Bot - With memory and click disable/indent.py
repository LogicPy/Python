# Define the path to the original file and the output file
input_file_path = 'simple-counter.txt'

# Read the file content first
try:
    with open(input_file_path, 'r') as file:
        lines = file.readlines()  # Read all lines into a list

    # Process lines to remove leading whitespace
    cleaned_lines = [line.lstrip() for line in lines]

    # Write the cleaned lines to the same file or a new file after processing is confirmed
    if cleaned_lines:  # Ensure there is content to write
        with open(input_file_path, 'w') as file:  # Change path here if you want to write to a new file
            file.writelines(cleaned_lines)
        print("Indentations removed and file saved.")
    else:
        print("No content to write after processing.")
except IOError as e:
    print(f"An error occurred while reading or writing the file: {e}")

